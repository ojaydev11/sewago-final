/**
 * Check if a route should be dynamic
 */
export function shouldBeDynamic(route: string): boolean {
  return DYNAMIC_ROUTES.some(dynamicRoute => 
    route.startsWith(dynamicRoute) || 
    route === dynamicRoute ||
    route.includes(dynamicRoute.replace('/', ''))
  );
}

/**
 * Get the appropriate dynamic export for a page
 */
export function getDynamicExports(route: string) {
  if (shouldBeDynamic(route)) {
    return {
      dynamic: 'force-dynamic' as const,
      generateStaticParams: preventStaticGeneration,
    };
  }
  
  return {};
}
